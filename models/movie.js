const mongoose = require('mongoose');
const { patternUrl } = require('../helpers/constantsHelpers');

const movieSchema = new mongoose.Schema({
  country: {
    type: String,
    required: true,
  },
  director: {
    type: String,
    required: true,
  },
  duration: {
    type: Number,
    required: true,
  },
  year: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    validate: (src) => patternUrl.test(src),
  },
  trailerLink: {
    type: String,
    validate: (src) => patternUrl.test(src),
  },
  thumbnail: {
    type: String,
    validate: (src) => patternUrl.test(src),
  },
  owner: {
    type: mongoose.Schema.Types.ObjectId,
    required: true,
    ref: 'user',
  },
  movieId: {
    type: Number,
    required: true,
  },
  nameRU: {
    type: String,
    required: true,
  },
  nameEN: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model('movie', movieSchema);
